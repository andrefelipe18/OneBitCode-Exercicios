let NavesParadas = ["Elemental", "Helmet"]
console.log(NavesParadas)

NavesParadas.push("Andromeda") //Adicionar um elemento no final do array
console.log(NavesParadas)

NavesParadas.pop() //Remover o ultimo elemento do Array
console.log(NavesParadas)

NavesParadas.shift() //Remove o primeiro elemento
console.log(NavesParadas)

NavesParadas.unshift("Phoenix") //Adiciona um elemento na posição 0
console.log(NavesParadas)

let HelmetNav = NavesParadas.indexOf("Helmet") //Saber a posição de algum elemento
console.log(HelmetNav)